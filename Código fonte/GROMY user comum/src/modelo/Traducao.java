public class Traducao {
    private int Id;
    private String textoPortugues;
    private String textoEspanhol;

    public int getId() {
        return Id;
    }

    public void setId(int Id) {
        this.Id = Id;
    }

    public String getTextoPortugues() {
        return textoPortugues;
    }

    public void setTextoPortugues(String textoPortugues) {
        this.textoPortugues = textoPortugues;
    }

    public String getTextoEspanhol() {
        return textoEspanhol;
    }

    public void setTextoEspanhol(String textoEspanhol) {
        this.textoEspanhol = textoEspanhol;
    }
    
    
}
