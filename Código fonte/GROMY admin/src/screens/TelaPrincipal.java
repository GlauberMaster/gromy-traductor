    Traducao palavraSelecionada = new Traducao();
    private void tabelaGerenciamentoPalavrasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaGerenciamentoPalavrasMouseClicked
        int linha  = this.tabelaGerenciamentoPalavras.getSelectedRow();
        codigoLinhaSelecionada.setText(tabelaGerenciamentoPalavras.getModel().getValueAt(linha, 0).toString());
        palavraSelecionada.setId(Integer.parseInt(tabelaGerenciamentoPalavras.getModel().getValueAt(linha, 0).toString()));
        palavraSelecionada.setTextoPortugues(tabelaGerenciamentoPalavras.getModel().getValueAt(linha, 1).toString());
        palavraSelecionada.setTextoEspanhol(tabelaGerenciamentoPalavras.getModel().getValueAt(linha, 2).toString());
        editarPalavra.setEnabled(true);
        deletarPalavra.setEnabled(true);
    }//GEN-LAST:event_tabelaGerenciamentoPalavrasMouseClicked

