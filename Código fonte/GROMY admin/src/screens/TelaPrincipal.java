    private void editarPalavraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarPalavraActionPerformed
        cadastrarCampoEspanhol.setText(palavraSelecionada.getTextoPortugues());
        cadastrarCampoPortugues.setText(palavraSelecionada.getTextoEspanhol());
        cadastrarBtn.setText("Atualizar Palavra");
        menuGuias.setSelectedIndex(1);
    }//GEN-LAST:event_editarPalavraActionPerformed

    private void deletarPalavraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletarPalavraActionPerformed
         TraducaoBO traducaoBO = new TraducaoBO();
         traducaoBO.excluir(palavraSelecionada);
         preencherTable();
    }//GEN-LAST:event_deletarPalavraActionPerformed

