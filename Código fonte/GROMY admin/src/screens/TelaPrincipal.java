    private void cadastrarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarBtnActionPerformed
       
        if("Atualizar Palavra".equals(cadastrarBtn.getText())){
            TraducaoBO traducaoBO = new TraducaoBO();
            palavraSelecionada.setTextoPortugues(cadastrarCampoPortugues.getText());
            palavraSelecionada.setTextoEspanhol(cadastrarCampoEspanhol.getText());
            traducaoBO.alterar(palavraSelecionada);
            cadastrarCampoEspanhol.setText("");
          cadastrarCampoPortugues.setText("");
            preencherTable();
            menuGuias.setSelectedIndex(2);
        }else{
             insertToTable();
        }
// TODO add your handling code here:
    }//GEN-LAST:event_cadastrarBtnActionPerformed
    
