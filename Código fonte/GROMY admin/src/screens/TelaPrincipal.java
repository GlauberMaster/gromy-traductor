    public void preencherTable(){
        DefaultTableModel modeltable = (DefaultTableModel) tabelaTraducoes.getModel();
        DefaultTableModel manageModeltable = (DefaultTableModel) tabelaGerenciamentoPalavras.getModel();
        
        modeltable.setNumRows(0);
        manageModeltable.setNumRows(0);
        
        TraducaoBO traducaoBO = new TraducaoBO();
        
        
        
        
        for (Traducao traducao : traducaoBO.consulta()){
           manageModeltable.addRow(new Object[]  {
                traducao.getId(), traducao.getTextoEspanhol(), traducao.getTextoPortugues()
                
            } );
      
            modeltable.addRow(new Object[]  {
                traducao.getId(), traducao.getTextoEspanhol(), traducao.getTextoPortugues()
                
            } );
            
                    
            }
        }
    
    public void insertToTable(){
        TraducaoBO traducaoBO = new TraducaoBO();
        Traducao traducao = new Traducao();
        traducao.setTextoPortugues(cadastrarCampoPortugues.getText());
        traducao.setTextoEspanhol(cadastrarCampoEspanhol.getText());
        
        if(!traducao.getTextoPortugues().isEmpty() &&  !traducao.getTextoEspanhol().isEmpty()){
          traducaoBO.incluir(traducao);
          preencherTable(); 
          
        }
    }

